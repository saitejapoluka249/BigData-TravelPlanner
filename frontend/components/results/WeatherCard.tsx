import React from 'react';

export default function WeatherCard({ weather }: { weather: any }) {
  // Check against the actual backend structure
  if (!weather || !weather.days || weather.days.length === 0) {
    return (
      <div className="p-8 text-center bg-gray-50 border border-dashed border-gray-200 rounded-2xl text-gray-500 font-bold italic">
        Weather data is currently unavailable for these dates.
      </div>
    );
  }

  // Helper to turn backend text like "Clear Sky" into an emoji
  const getWeatherIcon = (description: string) => {
    const desc = description.toLowerCase();
    if (desc.includes('clear') || desc.includes('sun')) return '☀️';
    if (desc.includes('cloud')) return '☁️';
    if (desc.includes('rain') || desc.includes('drizzle')) return '🌧️';
    if (desc.includes('thunder') || desc.includes('storm')) return '⛈️';
    if (desc.includes('snow')) return '❄️';
    if (desc.includes('mist') || desc.includes('fog')) return '🌫️';
    return '⛅'; // default
  };

  // Helper to format "2024-10-15" into "Tue, Oct 15"
  const formatDate = (dateString: string) => {
    const date = new Date(dateString);
    // Using UTC ensures the date doesn't accidentally shift backward a day based on local browser time
    return date.toLocaleDateString('en-US', { weekday: 'short', month: 'short', day: 'numeric', timeZone: 'UTC' });
  };

  return (
    <div className="bg-white rounded-xl border border-gray-200 shadow-sm p-5">
      <h3 className="text-2xl font-black text-gray-900 tracking-tight mb-4">Trip Forecast</h3>
      
      {/* Display the overall summary generated by the backend */}
      {weather.overall_summary && (
        <div className="mb-6 p-4 bg-blue-50/50 border border-blue-100 rounded-lg text-blue-800 text-sm font-medium">
          ℹ️ {weather.overall_summary}
        </div>
      )}

      <div className="flex gap-4 overflow-x-auto pb-4 custom-scrollbar">
        {weather.days.map((day: any, idx: number) => (
          <div key={idx} className="min-w-[140px] border border-gray-100 rounded-xl p-4 text-center bg-gray-50 hover:bg-blue-50 hover:border-blue-200 transition-colors shadow-sm shrink-0 flex flex-col justify-between">
            <div>
              <p className="font-black text-sm text-gray-800 mb-2 uppercase tracking-wider">
                {formatDate(day.date)}
              </p>
              <div className="text-4xl mb-3 drop-shadow-sm">
                {getWeatherIcon(day.weather)}
              </div>
              <p className="text-[11px] text-gray-500 font-bold uppercase leading-tight mb-3 px-1">
                {day.weather}
              </p>
            </div>
            
            <div className="flex justify-center items-center gap-3 pt-3 border-t border-gray-200/60 mt-auto">
              <div className="flex flex-col">
                <span className="text-[10px] text-red-400 font-bold uppercase">High</span>
                <span className="font-black text-red-600 text-lg leading-none">
                  {Math.round(day.max_temp)}°
                </span>
              </div>
              <div className="flex flex-col">
                <span className="text-[10px] text-blue-400 font-bold uppercase">Low</span>
                <span className="font-black text-blue-600 text-lg leading-none">
                  {Math.round(day.min_temp)}°
                </span>
              </div>
            </div>
          </div>
        ))}
      </div>
    </div>
  );
}